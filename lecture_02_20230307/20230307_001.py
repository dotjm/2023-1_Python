# 반지름이 rad인 피자의 면적을 구해보자

import math # math 라이브러리 가져오기

rad = 10

area = 3.14 * rad * rad
peri = 2 * 3.14 * rad

print("Area = " + str(area), " Perimeter = ", peri)

print("Area = ", area, " Perimeter = ", peri)

